export const mergeServices = (apiList) => {
  const apiPaid = apiList.filter(s => s.price != null && Number(s.price) > 0);
  const apiNames  = new Set(apiPaid.map(s => s.name));
  const apiImages = new Set(apiPaid.map(s => IMAGE_MAP[s.name]).filter(Boolean));
  const staticExtra = STATIC_SERVICES.filter(s => {
    const img = IMAGE_MAP[s.name];
    return !apiNames.has(s.name) && !(img && apiImages.has(img));
  });
  return [...apiPaid, ...staticExtra];
};
